export type Locale = "en" | "de" | "fr" | "it";

export const LOCALES: Locale[] = ["en", "de", "fr", "it"];

export function parseLocale(raw: string | null | undefined): Locale {
  const v = (raw || "").toLowerCase().slice(0, 2);
  if (v === "de" || v === "fr" || v === "it" || v === "en") return v;
  return "en";
}

